# შეამოწმე, არის თუ არა მომხმარებლის შემოტანილი ორი რიცხვი ერთმანეთზე მეტი და 10-ის ჯერადი.

num1 = int(input("Enter num: "))
num2 = int(input("Enter num: "))

if num1 > num2 % 10 == 0:
    print("10-ის ჯერადია ის რიცხვი რომელიც მეტია მეორეზე.", num1)
elif num1 < num2 % 10 == 0:
    print("10-ის ჯერადია ის რიცხვი რომელიც მეტია მეორეზე.", num2)