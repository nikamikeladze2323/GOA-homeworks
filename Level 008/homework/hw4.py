# დაწერე პროგრამა, რომელიც ამოწმებს, არის თუ არა მომხმარებლის შემოტანილი რიცხვი 100-ზე მეტი და ლუწი.

num = int(input("Enter num: "))

if num > 100 and num % 2 == 0:
    print("შენი რიცხვი მეტია ასსზე და ლუწია")
elif num > 100:
    print("შენი რიცხვი მეტია ასსზე და კენტია")
else:
    print("შენი რიცხვი ნაკლებია 100-ზე")