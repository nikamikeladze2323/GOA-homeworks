# 2) მომხმარებელს შემოატანინეთ რიცხვი და შემდეგ for loop - ის გამოყენებით გამოიტანეთ ამ რიცხვამდე ყველა რიცხვის 
# ჯამი(მაგალითად თუ შეიყვანს 10ს გამოიტანეთ 10მდე ყველა რიცხვის ჯამი მაგალითად).

input1 = int(input("Enter number: "))

sum_of_nums = 0

for i in range(1, input1, +1):
    sum_of_nums += i

print(sum_of_nums)
    