# ისე რომ ლუწიც იყოს და 10-ზე ნაკლებიც, 
# გამოიყენეთ for loop

numbers = [3, 8, 12, 4, 7, 18, 6, 10, 15, 2, 9, 20, 1, 16, 13, 5, 14, 11, 17, 22]

final_numbers = []

for i in numbers:
    if i < 10 and i % 2 == 0:
        final_numbers.append(i)

print(final_numbers)