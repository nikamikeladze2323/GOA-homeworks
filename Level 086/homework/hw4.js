let mood = prompt("როგორ გრძნობთ თავს? (ბედნიერი, მოწყენილი, გაბრაზებული, ენთუზიაზმით სავსე)").toLowerCase();

switch (mood) {
    case "ბედნიერი":
        console.log("✨ შესანიშნავია! გააგრძელე ღიმილი 😃");
        break;
    case "მოწყენილი":
        console.log("💙 ყველაფერი უკეთესობისკენ შეიცვლება, ყოველთვის არის სინათლე.");
        break;
    case "გაბრაზებული":
        console.log("🔥 სჯობს ღრმა ჩასუნთქვა და სიმშვიდე მოძებნო.");
        break;
    case "ენთუზიაზმით სავსე":
        console.log("🚀 ეს შენი მომენტია! გამოიყენე ენერგია მიზნის მისაღწევად.");
        break;
    default:
        console.log("🤔 ეს ემოცია სიაში არ არის, სცადე ხელახლა!");
}
