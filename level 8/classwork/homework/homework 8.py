box = 150 # მომხმარებელის ყუთების რაოდენობა
computer = 240 # მომხამარებელის კომპიუტერების რაოდენობა

print(box >= computer) # ჩვენ აქ გვიწერია მეტია ანტოლია ეს False იმიტომ წერს რომ ყუთების რაოდენობა არ უდრის კომპიუტერების რაოდენობას და ასევე ყუთები არ არის კომპიუტერებზე ბევრი.
