print(box != computer) # აქ იმიტომ არის True რომ 150 და 240 ერთმანეთს არ უდრის.
print(box == computer) # აქ False იმიტომ არის რომ 150 და 240 ერთმანეთის ტოლები არ არის.
print(box < computer) # ეს იმიტომ არის True რადგან 150 ნაკლებია 240-ზე.
print(box > computer) # აქ კი პირიქით არის და მაგიტომ არის False. 

